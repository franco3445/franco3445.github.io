// 2. For Loops
    // for loops are repeated until a specified condition become false
        // for ([initial expression]; [condition]; [increment expression])
             //  {statment}
            // Initial expression would be our starting point in out loop
            // Most times we start at zero (zero index) unless specified
            // Condition is the stopping point for our loop
            // once condition becomes false, the loop will terminate
            // Statment is then executed while condition remains true
            // lastly, increment expression is executed and our condition
            // (cont.) is check again. 
